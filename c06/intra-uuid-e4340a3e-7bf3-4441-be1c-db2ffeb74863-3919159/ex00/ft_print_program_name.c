#include <unistd.h>

void	ft_print_program_name(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
	write(1, "\n", 1);
}

int	main(int argc, char	*argv[])
{
	char	*str;
	int		c;

	str = argv[0];
	c = argc;
	ft_print_program_name(str);
	return (0);
}
